using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Thaumaturgy.Clases
{
    public class FuncionesGenerales
    {
        public static string BoolToString(bool val)
        {
            if (val)
            {
                return "true";
            }
            else
            {
                return "false";
            }
        }
    }
}
